# Operators in Python
#  Operators are special symbols that perform operations on variables and values. 

# Arithmetic Operators
# Arithmetic operators are used to perform mathematical operations like addition, subtraction, multiplication, etc.

#Example

#Variables
a = 100
b = 5

#Addition 
print("Addition :", a + b)

#Substraction
print("Substraction:", a - b)

#Multiplication
print("Multiplication:", a * b)

# Division
print("Division:", a / b)  # '/'- true divison - Always returns float (even if the result is a whole number) -Keeps the fractional or decimal part

# Floor Division
print("Floor Division:", a // b)  # '//' - floor division, Returns int if both operands are integers, otherwise float - Removes the fractional part (rounds down)

# Modulus
print("Modulus:", a % b) 

# Exponentiation
print("Exponentiation:", a ** b)
